import streamlit as st
import subprocess
import shutil
import re
from pathlib import Path

# ==================================================
# Streamlit setup
# ==================================================
st.set_page_config(layout="wide")
st.title("Steel System Optimization (AMPL)")
st.caption("Edit parameters → Run AMPL → View results")

# ==================================================
# Paths
# ==================================================
BASE_DIR = Path(__file__).parent
PARAM_FILE = BASE_DIR / "parameters.mod"
USER_PARAM_FILE = BASE_DIR / "user_parameters.mod"
RUN_FILE = BASE_DIR / "run_ampl.run"

# ==================================================
# Sanity checks
# ==================================================
for f in [PARAM_FILE]:
    if not f.exists():
        st.error(f"Missing required file: {f.name}")
        st.stop()

AMPL_EXE = shutil.which("ampl")
if not AMPL_EXE:
    st.error("AMPL executable not found in PATH")
    st.stop()

# ==================================================
# Extract scalar DEFAULT parameters
# ==================================================
param_pattern = re.compile(
    r"^\s*param\s+(\w+)\s+default\s+([0-9.eE+-]+)\s*;",
    re.IGNORECASE
)

def load_default_parameters():
    params = {}
    with open(PARAM_FILE, "r") as f:
        for line in f:
            m = param_pattern.match(line)
            if m:
                params[m.group(1)] = float(m.group(2))
    return params

params = load_default_parameters()

if not params:
    st.error(
        "No tunable parameters found.\n\n"
        "Parameters must be declared as:\n"
        "param x default 10;"
    )
    st.stop()

# ==================================================
# Sidebar: parameter editor
# ==================================================
st.sidebar.header("Model Parameters")

user_params = {
    k: st.sidebar.number_input(k, value=v, format="%.6f")
    for k, v in sorted(params.items())
}

# ==================================================
# Write user_parameters.mod (LET overrides)
# ==================================================
def write_user_parameters(p):
    with open(USER_PARAM_FILE, "w") as f:
        f.write("# Auto-generated by Streamlit\n\n")
        for k, v in p.items():
            f.write(f"let {k} := {v};\n")

# ==================================================
# Write run file (FORCED OUTPUT)
# ==================================================
def write_run_file():
    with open(RUN_FILE, "w") as f:
        f.write(
            'printf "\\n=== AMPL START ===\\n";\n'
            "reset;\n\n"
            'printf "Including parameters.mod\\n";\n'
            "include parameters.mod;\n\n"
            'printf "Including user_parameters.mod\\n";\n'
            "include user_parameters.mod;\n\n"
            'printf "Including main.mod (defines T + solve)\\n";\n'
            "include main.mod;\n\n"
            'printf "\\n=== AMPL END ===\\n";\n'
        )

# ==================================================
# Run AMPL
# ==================================================
if st.button("Run Optimization", type="primary"):

    write_user_parameters(user_params)
    write_run_file()

    with st.spinner("Running AMPL..."):
        result = subprocess.run(
            [AMPL_EXE, "run_ampl.run"],
            cwd=str(BASE_DIR),
            capture_output=True,
            text=True
        )

    st.subheader("AMPL STDOUT")
    st.text(result.stdout if result.stdout else "<empty>")

    st.subheader("AMPL STDERR")
    st.text(result.stderr if result.stderr else "<empty>")

    st.subheader("Return Code")
    st.write(result.returncode)

    if result.returncode == 0:
        st.success("Optimization completed")
    else:
        st.error("AMPL execution failed")
